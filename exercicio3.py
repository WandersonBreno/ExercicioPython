#Faça um Programa que peça dois números e imprima a soma.
print("Exercício de soma entre dois números")

numero1 = int(input("Digite o primeiro número: ")) #entrada número 1 digitado pelo usuário
numero2 = int(input("Digite o segundo número: ")) #entrada número 2 digitado pelo usuário


print(f'A soma entre os número digitados é: {numero1 + numero2}') #saída. resultado da soma entre os dois números